package _dto;

public class CtgDTO {
	private int top_ctg_code;
	private String top_ctg_name;
	private int sub_ctg_code;
	private String sub_ctg_name;
	public int getTop_ctg_code() {
		return top_ctg_code;
	}
	public String getTop_ctg_name() {
		return top_ctg_name;
	}
	public int getSub_ctg_code() {
		return sub_ctg_code;
	}
	public String getSub_ctg_name() {
		return sub_ctg_name;
	}
	public void setTop_ctg_code(int top_ctg_code) {
		this.top_ctg_code = top_ctg_code;
	}
	public void setTop_ctg_name(String top_ctg_name) {
		this.top_ctg_name = top_ctg_name;
	}
	public void setSub_ctg_code(int sub_ctg_code) {
		this.sub_ctg_code = sub_ctg_code;
	}
	public void setSub_ctg_name(String sub_ctg_name) {
		this.sub_ctg_name = sub_ctg_name;
	}
	
}
